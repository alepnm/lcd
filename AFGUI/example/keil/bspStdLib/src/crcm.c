unsigned short Crc16ModBus(unsigned char * pcBlock, unsigned int len)
{
    
    unsigned short crc = 0xFFFF;
 	
    while (len--)
        crc = (crc >> 8) ^ Crc16TableModBus[(crc & 0xFF) ^ *pcBlock++];
    return crc;
}

unsigned short Crc16ModBusNext(unsigned char * pcBlock, unsigned int len, unsigned short crc)
{
   
    while (len--)
        crc = (crc >> 8) ^ Crc16TableModBus[(crc & 0xFF) ^ *pcBlock++];
    return crc;
}






